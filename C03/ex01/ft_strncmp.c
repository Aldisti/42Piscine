int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				res;

	res = 0;
	i = 0;
	while (i < n && res == 0)
	{
		if (s1[i] == '\0' || s2[i] == '\0')
		{
			res = s1[i] - s2[i];
			break ;
		}
		res = s1[i] - s2[i];
		i++;
	}
	return (res);
}
