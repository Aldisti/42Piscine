#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	if (argc <= 1)
		return (0);
	while (--argc)
	{
		i = -1;
		while (argv[argc][++i])
			write(1, &argv[argc][i], 1);
		write(1, "\n", 1);
	}
}
