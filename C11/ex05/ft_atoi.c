int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	m;

	i = 0;
	j = 0;
	m = 1;
	while ((str[j] >= 9 && str[j] <= 13) || str[j] == 32)
		j++;
	while (str[j] == '+' || str[j] == '-')
	{
		if (str[j] == '-')
			m = -m;
		j++;
	}
	while (str[j] >= '0' && str[j] <= '9')
	{
		i *= 10;
		i += str[j] - '0';
		j++;
	}
	i *= m;
	return (i);
}
