#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = nb * -1;
		write(1, "-", 1);
	}
	c = (nb % 10) + 48;
	nb = nb / 10;
	if (nb > 0)
		ft_putnbr(nb);
	write(1, &c, 1);
}
